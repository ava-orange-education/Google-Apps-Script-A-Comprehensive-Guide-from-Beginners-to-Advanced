function arithmeticOperators() {
  // Addition
  Logger.log('3 + 2 = ' + (3 + 2)); // 3 + 2 = 5
  // Subtraction
  Logger.log('3 - 2 = ' + (3 - 2)); // 3 - 2 = 1
  // Multiplication
  Logger.log('3 * 2 = ' + 3 * 2); // 3 * 2 = 6
  // Division
  Logger.log('3 / 2 = ' + 3 / 2); // 3 / 2 = 1.5
  // Exponentiation
  Logger.log('3 ** 2 = ' + 3 ** 2); // 3 ** 2 = 9
  // Modulus division (returns the remainder)
  Logger.log('3 % 2 = ' + (3 % 2)); // 3 % 2 = 1,
}
